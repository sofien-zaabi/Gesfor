 private void update_fct()
 {
  DefaultListModel n=new  DefaultListModel();


  try{





	    		PreparedStatement pstmt1 = conn.prepareStatement("select nom_formation from formation_ct  ");

          rs = pstmt1.executeQuery();
          while(rs.next())
          {
              String nf=rs.getString("nom_formation");

              n.addElement("FCT:"+""+nf+" ");


          }
jList1.setModel(n);






	    }
	    	catch(Exception c){
	    		System.out.println(c.getMessage());

	    	}



 }
